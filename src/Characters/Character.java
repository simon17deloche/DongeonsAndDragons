package Characters;

public class Character {

    String Name;
    int Health;
    int MaxHealth;
    int Strength;
    int MaxStrength;

    public Character(int Health, int MaxHealth, int Strength, int MaxStrength) {

        this.Health = Health;
        this.MaxHealth = MaxHealth;
        this.Strength = Strength;
        this.MaxStrength = MaxStrength;
    }
}

